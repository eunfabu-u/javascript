//2. Getter and Setter
class User {
  constructor(firstName, lastName, age) {
    this.firstName = firstName;
    this.lastName = lastName;
    this.age = age;
  }
  get age() {
    return this._age;
  }
  set age(value) {
    // if (value < 0) {
    //   throw Error('age can not be negative');
    // }
    // this._age = value;
    this._age = value < 0 ? 0 : value;
  }
}
const user1 = new User('Steve', 'Jobs', -1);
console.log(user1.age);

